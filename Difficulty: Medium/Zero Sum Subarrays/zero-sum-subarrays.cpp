class Solution {
  public:
    int findSubarray(vector<int> &arr) {
        unordered_map<int,int> mp;
        int x = 0, ans = 0; mp[0] = 1;
        for(auto it: arr) {
            x += it;
            ans += mp[x];
            mp[x]++;
        }
        return ans;
    }
};

